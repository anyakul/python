# Арифметические операции
# +, -, *, /, %, //, **, ⊕, ⊖, (), сокращенные операции

a = 8
b = 3
c = 1.3
d = 3
c = a + b
d = a - b
e = a * b
f = round(a / b, 2)
g = a // b
h = a % b
i = a ** b

print('Сложение =', c)
print('Вычитание =', d)
print('Умножение =', e)
print('Деление =', f)
print('Целочисленное деление =', g)
print('Остаток от деления =', h)
print('Возведение в степень =', i)
